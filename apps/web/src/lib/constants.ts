export function getStockImage(
  category: keyof typeof STOCK_IMAGES,
  index = 0,
  sig?: string
): string {
  const images = STOCK_IMAGES[category];
  const baseUrl = images[index % images.length] || images[0];
  
  if (sig) {
    const url = new URL(baseUrl);
    url.searchParams.set("sig", sig);
    return url.toString();
  }
  
  return baseUrl;
}
